<h2>Acerca de nosotros</h2>
<p><strong>MISIÓN: </strong>La revista Digital Auténtica tiene como misión fundamental resaltar las cualidades, virtudes, de las mujeres de la zona sur de Anzoátegui (Venezuela), así como a personalidades femeninas de la esfera nacional que muestren un rostro de Mujer Triunfadora, con un implícito mensaje de “Yo si puedo”, “Yo si valgo”, “Yo soy una super mujer” “ Yo soy auténtica”.</p><p>


<strong>VISIÓN:</strong> Estamos orientados a  exaltar la imagen de nuestras mujeres, talentosas manejando un claro concepto de “No más maltratos a las Mujeres.” De igual manera, se persigue hacer una muestra de todo el potencial del oriente Venezolano, como emporio de riqueza natural y humana.</p><p>


<strong>VALORES: </strong>Nuestros valores consisten en la confianza, el respeto y la transparencia reflejando nuestra responsabilidad de la gran misión de vida en ser mujeres tanto físicamente como en esencia.</p><p>

<strong>QUIENES SOMOS: </strong>Un equipo integral conformado por  profesionales de la Comunicación, Periodistas, Diseñadores Gráficos y Fotógrafos. Dispuestos a llevarles a ustedes en todos nuestros trabajos lo hermoso del oriente venezolano, acompañado del rostro de nuestras féminas.</p><p>

